function mostrar(){
    document.getElementById('Mostrar_caja-2').style.display= 'none';
    document.getElementById('caja-1').style.display = 'none';
    document.getElementById('Ocultar_caja-2').style.display= 'block';
    document.getElementById('caja-2').style.display = 'block';
    };
function ocultar() {
    document.getElementById('Mostrar_caja-2').style.display= 'block';
    document.getElementById('caja-1').style.display = 'block';
    document.getElementById('Ocultar_caja-2').style.display= 'none';
    document.getElementById('caja-2').style.display = 'none';
    };